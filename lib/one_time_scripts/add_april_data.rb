module AddAprilData

  extend self

  def add

    bfc = Farmer.count

    big_arr = get_big_arr

    big_arr.each do |arr|
      name = arr[0]
      phone_number = arr[1]
      id_number = arr[2]
      farmer_group = arr[3]
      maize_kg = arr[4]
      fertilizer_kg = arr[5]

      farmer = get_farmer(phone_number, id_number)
      if farmer.nil?
        farmer = create_farmer(name, phone_number, id_number, farmer_group)
      end

      if maize_kg.present?
        create_maize_loan(maize_kg)
        update_farmer_loan_status(farmer)
      end

      if fertilizer_kg.present?
        create_fertilizer_loan(fertilizer_kg)
        update_farmer_loan_status(farmer)
      end

    end

    puts "BEFORE FARMERS #{bfc}"
    puts "AFTER FARMERS #{Farmer.count}"
  end

  def get_farmer(phone_number, id_number)
    farmer = nil
    if (phone_number != '700000000') && (phone_number.length == 9)
      if Farmer.where(phone_number: "+254" + phone_number).exists?
        farmer = Farmer.where(phone_number: "+254" + phone_number).first
      end
    else
      if (id_number != '0') && (id_number.length > 3)
        if Farmer.where(national_id_number: id_number).exists?
          farmer = Farmer.where(national_id_number: id_number).first
        end
      end
    end
    return farmer
  end



  def create_maize_loan(maize_kg)
    Loan.new(commodity: 'inputs',
             commodities_lent: {commodity: 'maize', quantity: maize_kg, unit: 'kg'},
             time_period: 'before planting',
             season: $current_season,
             interest_rate: 2.0,
             interest_period: "month",
             interest_type: "simple",
             duration: 6,
             duration_unit: "month",
             currency: "KES",
             service_charge: 0.0,
             structure: "installments",
             status: "application started",
             disbursed_date: Time.new(2017, 4),
             disbursal_method: "supplier",
             repayment_method: "mpesa")
  end

  def create_fertilizer_loan(fertilizer_kg)
    Loan.new(commodity: 'inputs',
             commodities_lent: {commodity: 'fertilizer', quantity: fertilizer_kg, unit: 'kg'},
             commodity_quantity: fertilizer_kg,
             time_period: 'before planting',
             season: $current_season,
             interest_rate: 2.0,
             interest_period: "month",
             interest_type: "simple",
             duration: 6,
             duration_unit: "month",
             currency: "KES",
             service_charge: 0.0,
             structure: "installments",
             status: "application started",
             disbursed_date: Time.new(2017, 4),
             disbursal_method: "supplier",
             repayment_method: "mpesa")
  end


  def update_farmer_loan_status(farmer)
    farmer.received_loans = true
    farmer.accepted_loan_tnc = true
    farmer.save
  end


  def create_farmer(name, phone_number, id_number, farmer_group)
    f = Farmer.new(name: name,
                   association_name: farmer_group,
                   county: 'pre-determined')
    if (phone_number.length == 9) && (phone_number != '700000000')
      f.phone_number = phone_number
    end

    if (id_number != '0') && (id_number.length > 4)
      f.national_id_number = id_number
    end

    f.save
    return f
  end

